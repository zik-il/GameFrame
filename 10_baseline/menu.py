import pygame
from GameFrame import Globals
from player import Player
from spritesheet import Spritesheet

class menu():
    def __init__(self, game):
        self.game = game
        self.mid_w, self.mid_h = self.game.DISPLAY_W / 2, self.game.DISPLAY_H / 2
        self.run_display = True
        self.cursor_rect = pygame.Rect(0, 0, 20, 20)
        self.offset = - 100

    def draw_cursor(self):
        self.game.draw_text('*', 15, self.cursor_rect.x, self.cursor_rect.y)

    def blit_screen(self):
        self.game.window.blit(self.game.display, (0, 0))
        pygame.display.update()
        self.game.reset_keys()

class MainMenu(menu):
    def __init__(self, game):
        menu.__init__(self, game)
        self.state = "Start"
        self.playerx, self.playery = self.mid_w, self.mid_h - 50
        self.startx, self.starty = self.mid_w, self.mid_h + 30
        self.optionsx, self.optionsy = self.mid_w, self.mid_h + 50
        self.creditsx, self.creditsy = self.mid_w, self.mid_h + 70
        self.cursor_rect.midtop = (self.startx + self.offset, self.starty)

    def display_menu(self):

        self.run_display = True
        #create animation
        anim = Player((self.playerx, self.playery)) 
        anim.LEFT_KEY= False
        anim.RIGHT_KEY= False
        while self.run_display:
            self.game.check_events()

            #update animation status
            if self.game.LEFT_KEY:
                anim.LEFT_KEY, anim.FACING_LEFT = True, True
            else:
                anim.LEFT_KEY= False
                
            if self.game.RIGHT_KEY:
                anim.RIGHT_KEY, anim.FACING_LEFT = True, False
            else:
                anim.RIGHT_KEY = False

            anim.update()

            self.game.display.fill(self.game.BLACK)
            self.game.draw_text('Main Menu', 20, self.game.DISPLAY_W / 2, self.game.DISPLAY_H / 2 - 20)
            start_rect = self.game.draw_text("Start Game", 20, self.startx, self.starty)
            options_rect = self.game.draw_text("Options", 20, self.optionsx, self.optionsy)
            credits_rect = self.game.draw_text("Credits", 20, self.creditsx, self.creditsy)

            anim.draw(self.game.display)  #draw animation

            #check collision and button clicked
            mx, my = pygame.mouse.get_pos()
            #self.game.START_KEY = False

            if start_rect.collidepoint((mx, my)):
                if self.game.Click:
                    print("start game clicked")
                    self.state = 'Start'
                    self.game.START_KEY = True

            if options_rect.collidepoint((mx, my)):
                if self.game.Click:
                    print("options menu clicked")
                    self.state = 'Options'
                    self.game.START_KEY = True


            if credits_rect.collidepoint((mx, my)):
                if self.game.Click:
                    print("credits menu clicked")
                    self.state = 'Credits'
                    self.game.START_KEY = True


            self.game.Click = False # reset button clicked

            self.check_input()        
            self.draw_cursor()
            self.blit_screen()


    def move_cursor(self):
        if self.game.DOWN_KEY:
            if self.state == 'Start':
                self.cursor_rect.midtop = (self.optionsx + self.offset, self.optionsy)
                self.state = 'Options'
            elif self.state == 'Options':
                self.cursor_rect.midtop = (self.creditsx + self.offset, self.creditsy)
                self.state = 'Credits'
            elif self.state == 'Credits':
                self.cursor_rect.midtop = (self.startx + self.offset, self.starty)
                self.state = 'Start'
        elif self.game.UP_KEY:
            if self.state == 'Start':
                self.cursor_rect.midtop = (self.creditsx + self.offset, self.creditsy)
                self.state = 'Credits'
            elif self.state == 'Options':
                self.cursor_rect.midtop = (self.startx + self.offset, self.starty)
                self.state = 'Start'
            elif self.state == 'Credits':
                self.cursor_rect.midtop = (self.optionsx + self.offset, self.optionsy)
                self.state = 'Options'

    def check_input(self):
        self.move_cursor()
        if self.game.START_KEY:
            if self.state == 'Start':
                Globals.playing = True
            elif self.state == 'Options':
                self.game.curr_menu = self.game.options
            elif self.state == 'Credits':
                self.game.curr_menu = self.game.credits
            self.run_display = False

class OptionsMenu(menu):
    def __init__(self, game):
        menu.__init__(self, game)
        self.state = 'Volume'
        self.volx, self.voly = self.mid_w, self.mid_h + 20
        self.controlsx, self.controlsy = self.mid_w, self.mid_h + 40
        self.cursor_rect.midtop = (self.volx + self.offset, self.voly)

    def display_menu(self):
        self.run_display = True
        while self.run_display:
            self.game.check_events()
            self.check_input()
            self.game.display.fill((0, 0, 0))
            self.game.draw_text('Options', 20, self.game.DISPLAY_W / 2, self.game.DISPLAY_H / 2 - 30)
            self.game.draw_text("Volume", 15, self.volx, self.voly)
            self.game.draw_text("Controls", 15, self.controlsx, self.controlsy)
            self.draw_cursor()
            self.blit_screen()

    def check_input(self):
        if self.game.BACK_KEY:
            self.game.curr_menu = self.game.main_menu
            self.run_display = False
        elif self.game.UP_KEY or self.game.DOWN_KEY:
            if self.state == 'Volume':
                self.state = 'Controls'
                self.cursor_rect.midtop = (self.controlsx + self.offset, self.controlsy)
            elif self.state == 'Controls':
                self.state = 'Volume'
                self.cursor_rect.midtop = (self.volx + self.offset, self.voly)
